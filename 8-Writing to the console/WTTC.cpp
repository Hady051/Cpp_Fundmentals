# include <iostream>
#include <cmath>

using namespace std;

int main () {
    double f_result = floor(1.2);
    cout << "floor result = " << f_result << endl;

    double c_result = ceil(1.2);
    cout << "ceil result = " << c_result << endl;

    double power_result = pow(2,3);
    cout << "power_result = " << power_result << endl;

    return 0;
}
